"""F4 — 3-tier table detector.

Tier-1: Excel Tables (`<table>` parts in `xl/tables/tableN.xml`).
        Discovered through `ws.tables` (openpyxl exposes them as a
        `TableList` keyed by displayName).
Tier-2: sheet-scope named ranges (`<definedName localSheetId=...>`).
        Workbook-scope names are silently ignored per honest-scope (d).
Tier-3: gap-detection (consecutive empty rows ≥ `gap_rows` AND/OR
        consecutive empty cols ≥ `gap_cols`) over the area not already
        claimed by Tier-1 / Tier-2.

Mode semantics:
- "auto"        : 1 → 2 → 3 fallthrough (default).
- "tables-only" : Tier-1 + Tier-2 only; no gap-detection.
- "whole"       : a single region spanning `ws.dimensions`.

UC-03 A4 (ListObject vs named range overlap): Tier-1 wins — Tier-2
ranges that overlap a Tier-1 region are dropped. Tier-3 is similarly
bounded by previously-claimed bounding boxes.
"""

from __future__ import annotations

import json
import re
from pathlib import Path
from typing import Any

from openpyxl.utils.cell import range_boundaries

from ._types import TableDetectMode, TableRegion

# ---------------------------------------------------------------------------
# Reserved defined-name filter (TASK 010 §11 patch)
# ---------------------------------------------------------------------------
#
# Source of truth lives in `_reserved_names.json` alongside this module.
# Loaded once when this module is first imported (i.e. on the first
# `xlsx_read.detect_tables` call — `xlsx_read.__init__` does NOT eagerly
# import `_tables`; the JSON read happens lazily at first-use, not at
# package-import).
#
# A defined name that matches any of these patterns is an Excel-internal
# artefact (built-in print area, autofilter range, saved Custom View
# state) and MUST NOT be emitted as a user table by
# `_named_ranges_for_sheet`. Patterns are compiled with `re.IGNORECASE`
# because OOXML §18.2.5 defined names compare case-insensitively in
# Excel — `_XLNM.Print_Area` and `_xlnm.print_area` are the same name.
#
# **Trust boundary:** `_reserved_names.json` is a shipped data file and
# shares the package's trust boundary. Tampering implies a compromised
# installation; the loader does not defend against an attacker who can
# already write into `site-packages`.

_RESERVED_NAMES_PATH = Path(__file__).parent / "_reserved_names.json"

# ReDoS shapes rejected at load time (inlined for parity with
# `xlsx_check_rules.constants.REDOS_REJECT_PATTERNS`; intentionally not
# imported across packages to keep `xlsx_read` self-contained).
_REDOS_REJECT_SHAPES: tuple[re.Pattern[str], ...] = (
    re.compile(r"\(.+\+\)\+"),
    re.compile(r"\(.+\*\)\*"),
    re.compile(r"\(.+\|.+\)\+"),
    re.compile(r"\(.+\|.+\)\*"),
)


def _load_reserved_name_matchers() -> tuple[re.Pattern[str], ...]:
    """Compile reserved-name regexes from `_reserved_names.json`.

    Fails loudly on a missing file, invalid JSON, an invalid regex, a
    pattern matching a known catastrophic-backtracking shape, or a
    missing `regex` key — these are packaging errors, not runtime data,
    and must surface immediately on first-use rather than corrupt the
    filter silently.

    Patterns are compiled with `re.IGNORECASE` because OOXML defined
    names are case-insensitive (see module-level comment).
    """
    with _RESERVED_NAMES_PATH.open("r", encoding="utf-8") as fp:
        spec = json.load(fp)
    out: list[re.Pattern[str]] = []
    for i, entry in enumerate(spec["patterns"]):
        if "regex" not in entry:
            raise KeyError(
                f"{_RESERVED_NAMES_PATH}: patterns[{i}] missing 'regex' key"
            )
        pat = entry["regex"]
        for shape in _REDOS_REJECT_SHAPES:
            if shape.search(pat):
                raise ValueError(
                    f"{_RESERVED_NAMES_PATH}: patterns[{i}] matches ReDoS "
                    f"shape {shape.pattern!r}: {pat!r}"
                )
        out.append(re.compile(pat, re.IGNORECASE))
    return tuple(out)


_RESERVED_NAME_MATCHERS: tuple[re.Pattern[str], ...] = _load_reserved_name_matchers()


# OOXML §18.2.6 caps defined-name length at 255 chars. Anything beyond
# this is either malformed or an attacker probe. Bounding input length
# kills the unbounded-per-name regex-match cost surfaced by
# /vdd-multi-3 security review (HIGH — without this, a workbook with
# 50K names × 100KB each → ~15 GB regex work).
_MAX_DEFINED_NAME_LEN: int = 255

# Zero-width and BOM characters that some workbook tools emit (and that
# attackers use to bypass naive filters). Stripped before matching.
_ZERO_WIDTH_CHARS: str = "​‌‍﻿"


def _is_reserved_name(name: str) -> bool:
    """Return True if `name` is an Excel-reserved defined name.

    Reserved names are auto-generated by Excel (built-in print/filter
    artefacts, Custom-View state) and must be excluded from user-facing
    table detection. See `_reserved_names.json` for the canonical
    pattern list and provenance. Comparison is case-insensitive and
    whitespace-tolerant (strips ASCII whitespace AND zero-width
    characters before matching) to catch malformed or third-party-
    emitter variants.

    **/vdd-multi-3 hardening:**
    - Input length bounded at `_MAX_DEFINED_NAME_LEN` (255 chars per
      OOXML §18.2.6) — names exceeding this are NOT reserved
      (matching skipped entirely, no regex cost on hostile input).
    - Zero-width characters (`U+200B`/`U+200C`/`U+200D`/`U+FEFF`)
      stripped alongside ASCII whitespace — closes the filter-bypass
      vector where attackers prepend ZWSP to a reserved name.

    **Conservative scope on length-cap-then-strip ordering:** the
    length cap fires BEFORE the strip, so a 300-char input with
    ZWSP padding wrapping a 16-char reserved payload is rejected
    (returns False, name surfaces as user-table). This matches OOXML
    §18.2.6 — the spec limit applies to the literal bytes the writer
    emitted, padding included. A 256-byte name is spec-illegal and
    therefore cannot be a built-in reserved name; an attacker who
    pads ZWSP to evade detection is denying themselves the filter,
    not bypassing security. Documented expected behaviour, locked
    by `test_long_input_not_matched_no_redos_surface`.
    """
    if not name:
        return False
    if len(name) > _MAX_DEFINED_NAME_LEN:
        # Spec-illegal length → cannot be a reserved built-in.
        return False
    stripped = name.strip().strip(_ZERO_WIDTH_CHARS)
    if not stripped:
        return False
    return any(m.match(stripped) for m in _RESERVED_NAME_MATCHERS)


def detect_tables(
    wb: Any,
    sheet_name: str,
    *,
    mode: TableDetectMode = "auto",
    gap_rows: int = 2,
    gap_cols: int = 1,
) -> list[TableRegion]:
    """Detect tables on `sheet_name`. See module docstring for tier rules."""
    if mode not in ("auto", "tables-only", "whole"):
        raise ValueError(f"Unknown mode: {mode!r}")
    ws = wb[sheet_name]

    if mode == "whole":
        return [_whole_sheet_region(ws, sheet_name)]

    tier1 = _listobjects_for_sheet(ws, sheet_name)
    tier2 = _named_ranges_for_sheet(wb, sheet_name, claimed=tier1)
    claimed = tier1 + tier2

    if mode == "tables-only":
        return claimed

    tier3 = _gap_detect(ws, sheet_name, claimed, gap_rows, gap_cols)
    return claimed + tier3


# ---------------------------------------------------------------------------
# Tier-1 — Excel Tables (ListObjects)
# ---------------------------------------------------------------------------


def _listobjects_for_sheet(ws: Any, sheet_name: str) -> list[TableRegion]:
    """Emit one region per `<table>` part bound to `ws`.

    openpyxl ≥ 3.1 surfaces tables via `ws.tables` — a `TableList`
    keyed by `displayName`. **Quirk:** `TableList.items()` yields
    `(name, ref_string)` pairs (NOT `(name, Table)`), while
    `TableList[name]` returns the full `Table` object — verified
    empirically against openpyxl 3.1.5. We iterate keys and look up
    each entry to get the `headerRowCount` attribute.
    """
    tables = getattr(ws, "tables", None)
    if not tables:
        return []
    out: list[TableRegion] = []
    for name in list(tables.keys()):
        table = tables[name]
        ref = getattr(table, "ref", None)
        if not ref:
            continue
        left_col, top_row, right_col, bottom_row = range_boundaries(ref)
        hrc = getattr(table, "headerRowCount", 1)
        # openpyxl uses `None` to mean "default = 1"; normalise.
        if hrc is None:
            hrc = 1
        out.append(
            TableRegion(
                sheet=sheet_name,
                top_row=top_row,
                left_col=left_col,
                bottom_row=bottom_row,
                right_col=right_col,
                source="listobject",
                name=name,
                listobject_header_row_count=hrc,
            )
        )
    return out


# ---------------------------------------------------------------------------
# Tier-2 — sheet-scope named ranges
# ---------------------------------------------------------------------------


def _named_ranges_for_sheet(
    wb: Any, sheet_name: str, claimed: list[TableRegion]
) -> list[TableRegion]:
    """Emit one region per sheet-scope `<definedName>` matching `sheet_name`.

    **openpyxl 3.1+ split:** sheet-scope names live on
    `ws.defined_names` (per-sheet `DefinedNameDict`); workbook-scope
    names live on `wb.defined_names`. The library reads ONLY from
    `ws.defined_names` so workbook-scope names are dropped by
    construction (honest-scope D8, no `localSheetId` filtering
    needed). Ranges that overlap a Tier-1 region are dropped
    (UC-03 A4).
    """
    ws = wb[sheet_name]
    out: list[TableRegion] = []
    sheet_defined = getattr(ws, "defined_names", None)
    if not sheet_defined:
        return out
    for name in list(sheet_defined):
        # **TASK 010 §11 patch:** skip Excel-reserved defined names
        # (built-in print areas, autofilter ranges, Custom-View state)
        # so they never surface as user tables. See
        # `_reserved_names.json` for the pattern list.
        if _is_reserved_name(name):
            continue
        dn = sheet_defined[name]
        # **L-M3 fix:** a defined name with multiple comma-separated
        # destinations on the same sheet yields multiple regions; we
        # disambiguate with `#1`, `#2` suffixes so downstream consumers
        # that index by `region.name` don't silently collide.
        dest_count = 0
        local_regions: list[TableRegion] = []
        for dest_sheet, dest_range in dn.destinations:
            if dest_sheet != sheet_name:
                # Destination resolves to a *different* sheet — skip.
                continue
            try:
                left_col, top_row, right_col, bottom_row = range_boundaries(dest_range)
            except (TypeError, ValueError):
                continue
            dest_count += 1
            local_regions.append(TableRegion(
                sheet=sheet_name,
                top_row=top_row,
                left_col=left_col,
                bottom_row=bottom_row,
                right_col=right_col,
                source="named_range",
                name=name,
            ))
        # Single-destination → name stays bare; multi-destination →
        # suffix with `#1`, `#2`, ... in iteration order.
        for i, region in enumerate(local_regions):
            final_name = (
                region.name if dest_count == 1
                else f"{region.name}#{i + 1}"
            )
            disambiguated = TableRegion(
                sheet=region.sheet,
                top_row=region.top_row,
                left_col=region.left_col,
                bottom_row=region.bottom_row,
                right_col=region.right_col,
                source=region.source,
                name=final_name,
            )
            if not _has_overlap(disambiguated, claimed):
                out.append(disambiguated)
    return out


