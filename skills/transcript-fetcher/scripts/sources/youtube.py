def _yt_dlp_command(yt_dlp_bin: Optional[str]) -> list[str]:
    """Build the base argv for invoking yt-dlp.

    If a binary path is provided, it is used directly. Otherwise we
    invoke ``python -m yt_dlp`` so the per-skill venv's installed
    ``yt-dlp`` package is used regardless of ``$PATH``.
    """
    if yt_dlp_bin:
        return [yt_dlp_bin]
    import sys as _sys

    return [_sys.executable, "-m", "yt_dlp"]


def _classify_failure(stderr: str) -> Optional[str]:
    """Return a short human-readable failure reason from yt-dlp stderr."""
    s = stderr.lower()
    for pat in _HARD_FAILURE_PATTERNS:
        if pat in s:
            return f"hard-failure ({pat})"
    for pat in _RATE_LIMIT_PATTERNS:
        if pat in s:
            return f"rate-limit ({pat})"
    return None


def _stderr_tail(stderr: str, n_lines: int = 5) -> str:
    """Return the last n non-empty *signal* lines of stderr, joined by ' | '.

    yt-dlp leaks a lot of advisory ``WARNING:`` lines (ffmpeg missing,
    impersonation-target absent, …) ahead of the actual ``ERROR:``.
    Including them in the tail bloats ``stat.notes`` and confuses
    downstream parsers, so warnings are filtered out — if the filtered
    result is empty we fall back to the raw tail so a pure-warning
    failure mode is still surfaced.
    """
    if not stderr:
        return ""
    all_lines = [line.strip() for line in stderr.splitlines() if line.strip()]
    signal = [
        ln for ln in all_lines
        if not ln.startswith("WARNING:")
        and "WARNING: " not in ln[:24]
    ]
    chosen = signal if signal else all_lines
    return " | ".join(chosen[-n_lines:])


def _try_download_subtitle(
    *,
    url: str,
    lang: str,
    kind: str,
    workdir: Path,
    pre_existing: set[Path],
    yt_dlp_bin: Optional[str],
    timeout_sec: int,
    cookies_file: Optional[Path] = None,
    with_info_json: bool = False,
) -> tuple[bool, Optional[Path], Optional[str]]:
    """Attempt to download one (kind, lang) subtitle track.

    Returns ``(success, vtt_path, note)``. ``note`` is a human-readable
    string about the attempt (added to TranscriptStat.notes regardless of
    success/failure for transparency, including the cause when yt-dlp
    fails for reasons other than missing-track).
    """
    out_tmpl = str(workdir / "%(id)s.%(ext)s")

    args = [
        *_yt_dlp_command(yt_dlp_bin),
        "--skip-download",
        "--sub-format", "vtt",
        "--sub-langs", lang,
        "--output", out_tmpl,
    ]
    if cookies_file is not None:
        args.extend(["--cookies", str(cookies_file)])
    if with_info_json:
        # Piggy-back the info-json on the subtitle call so a successful
        # ladder step yields both the .vtt and the metadata in one
        # subprocess round-trip — saves a second yt-dlp invocation for
        # the common --with-description case.
        args.append("--write-info-json")
    if kind == "manual":
        args.append("--write-subs")
    elif kind == "auto":
        args.append("--write-auto-subs")
    else:
        return False, None, f"unknown kind={kind!r}"
    # `--` terminates yt-dlp's flag parsing so URLs starting with `-`
    # (a malformed input or a `--cookies-from-browser`-shaped attack
    # string) are treated as a positional, not a flag.
    args.append("--")
    args.append(url)

    try:
        proc = subprocess.run(
            args,
            check=False,
            capture_output=True,
            text=True,
            timeout=timeout_sec,
        )
    except FileNotFoundError as e:
        return False, None, f"yt-dlp not found: {e}"
    except subprocess.TimeoutExpired:
        return False, None, f"timeout fetching {kind}:{lang} (>{timeout_sec}s)"

    failure_reason = _classify_failure(proc.stderr or "")
    if proc.returncode != 0 and failure_reason:
        # Hard or rate-limit failure: don't even bother checking the FS.
        tail = _stderr_tail(proc.stderr or "", 3)
        return False, None, f"{kind}:{lang} {failure_reason}: {tail}" if tail else f"{kind}:{lang} {failure_reason}"

    vtt = _find_new_vtt(workdir, lang, pre_existing)
    if vtt is not None:
        # Add this file to pre_existing so subsequent ladder steps don't
        # accidentally re-claim it.
        pre_existing.add(vtt.resolve())
        return True, vtt, f"got {kind}:{lang} -> {vtt.name}"

    if proc.returncode != 0:
        # Non-zero exit but no recognised failure phrase, and no file.
        tail = _stderr_tail(proc.stderr or "", 3)
        return False, None, (
            f"{kind}:{lang} yt-dlp rc={proc.returncode}: {tail}"
            if tail
            else f"{kind}:{lang} yt-dlp rc={proc.returncode}"
        )
    return False, None, f"no subtitle returned for {kind}:{lang}"


def _find_new_vtt(
    workdir: Path, lang: str, pre_existing: set[Path]
) -> Optional[Path]:
    """Find a freshly-downloaded VTT file matching ``lang`` in workdir.

    yt-dlp names files ``<id>.<lang>.vtt``. We match by suffix to avoid
    needing the video id up front, and exclude any path that was already
    in ``pre_existing`` before the call.
    """
    needle = f".{lang}.vtt"
    candidates = []
    for p in workdir.glob("*.vtt"):
        if p.name.endswith(needle) and p.resolve() not in pre_existing:
            candidates.append(p)
    if not candidates:
        return None
    # Prefer the most recently modified file when multiple match.
    candidates.sort(key=lambda p: p.stat().st_mtime, reverse=True)
    return candidates[0]


def _pick_fresh_info_json(
    workdir: Path, pre_existing: set[Path]
) -> Optional[dict]:
    """Return the most recent ``*.info.json`` written into ``workdir``.

    Used by :func:`fetch_youtube_transcript` to consume the info-json
    that piggy-backed on the subtitle call. Filters out any file that
    was already present before the call (snapshot semantics).
    """
    candidates = [
        p for p in workdir.glob("*.info.json")
        if p.resolve() not in pre_existing
    ]
    if not candidates:
        return None
    candidates.sort(key=lambda p: p.stat().st_mtime, reverse=True)
    try:
        return json.loads(candidates[0].read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError):
        return None


def _fetch_video_info(
    *,
    url: str,
    workdir: Path,
    yt_dlp_bin: Optional[str],
    timeout_sec: int,
    cookies_file: Optional[Path],
) -> Optional[dict]:
    """Run yt-dlp ``--write-info-json --skip-download`` and load the JSON.

    Returns the parsed info-json dict, or ``None`` if yt-dlp failed or
    no file was produced. yt-dlp writes the file as
    ``<id>.info.json`` in ``workdir``; we discover it by mtime so
    a pre-existing one from a prior call doesn't get re-claimed.
    """
    pre = {p.resolve() for p in workdir.glob("*.info.json")}
    args = [
        *_yt_dlp_command(yt_dlp_bin),
        "--skip-download",
        "--write-info-json",
        "--no-write-subs",
        "--no-write-auto-subs",
        "--output", str(workdir / "%(id)s.%(ext)s"),
    ]
    if cookies_file is not None:
        args.extend(["--cookies", str(cookies_file)])
    args.append("--")
    args.append(url)
    try:
        proc = subprocess.run(
            args,
            check=False,
            capture_output=True,
            text=True,
            timeout=timeout_sec,
        )
    except (FileNotFoundError, subprocess.TimeoutExpired):
        return None
    if proc.returncode != 0:
        # yt-dlp may leave a partial / stale .info.json on failure; do not
        # trust the FS when the process didn't exit cleanly.
        return None
    candidates = [
        p for p in workdir.glob("*.info.json") if p.resolve() not in pre
    ]
    if not candidates:
        return None
    candidates.sort(key=lambda p: p.stat().st_mtime, reverse=True)
    try:
        return json.loads(candidates[0].read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError):
        return None


def _write_youtube_description(
    *, info: dict, url: str, out_path: Path
) -> Path:
    """Write the ``.description.md`` sidecar from a yt-dlp info-json dict."""
    title = info.get("title") or "(untitled)"
    body = (info.get("description") or "").strip()
    frontmatter: dict = {
        "source": "youtube",
        "url": url,
        "video_id": info.get("id"),
        "title": title,
        "uploader": info.get("uploader") or info.get("channel"),
        "uploader_url": info.get("uploader_url") or info.get("channel_url"),
        "upload_date": _format_upload_date(info.get("upload_date")),
        "duration_sec": _coerce_int(info.get("duration")),
        "view_count": _coerce_int(info.get("view_count")),
        "like_count": _coerce_int(info.get("like_count")),
    }
    return write_description_md(
        out_path, frontmatter=frontmatter, title=title, body=body
    )


def _format_upload_date(raw: object) -> Optional[str]:
    """yt-dlp emits ``upload_date`` as ``YYYYMMDD`` — reshape to ISO."""
    if not isinstance(raw, str) or len(raw) != 8 or not raw.isdigit():
        return None
    return f"{raw[0:4]}-{raw[4:6]}-{raw[6:8]}"


def _coerce_int(value: object) -> Optional[int]:
    if isinstance(value, bool):
        return None
    if isinstance(value, int):
        return value
    if isinstance(value, float):
        return int(value)
    if isinstance(value, str) and value.isdigit():
        return int(value)
    return None


