"""YouTube source adapter.

Pulls caption tracks from a YouTube video URL via ``yt-dlp`` and
converts them to plain text using :mod:`_vtt_to_text`.

Subtitle language fallback (in priority order, configurable):

    manual ``ru`` -> auto ``ru-orig`` -> auto ``ru`` -> auto ``en``

If the only available track was an auto-translated English one, the
returned stat record carries a ``quality_flag`` of
``"english_auto_translation"`` so the SKILL.md flow can warn the user.
A ``quality_flag`` of ``"encoding_recovered"`` is set when the VTT
itself was not valid UTF-8 and a codec fallback was needed.

With ``with_description=True`` the adapter also runs yt-dlp once more
with ``--write-info-json --skip-download`` to fetch metadata (title,
uploader, upload date, duration, description) and writes a
``<out>.description.md`` sidecar — see :mod:`_description`.

This module is import-safe (no network calls at import time). The
``yt-dlp`` import is deferred until :func:`fetch_youtube_transcript`
runs, so unit tests for the VTT converter do not require yt-dlp to be
installed.
"""
from __future__ import annotations

import json
import re
import shutil
import subprocess
import tempfile
from pathlib import Path
from typing import Iterable, Optional

from ._description import description_path_for, write_description_md
from ._stat import (
    SourceRateLimitError,  # re-exported for callers # noqa: F401
    TranscriptFetchError,
    TranscriptStat,
    write_stat_sidecar,
)
from ._vtt_to_text import count_speaker_turns, vtt_file_to_plain_meta


# Default fallback ladder. Each item is (kind, lang) where kind is
# "manual" (user-uploaded) or "auto" (auto-generated by YouTube).
DEFAULT_FALLBACK_RU = (
    ("manual", "ru"),
    ("auto", "ru-orig"),
    ("auto", "ru"),
    ("auto", "en"),
)

DEFAULT_TIMEOUT_SEC = 180

# yt-dlp stderr fragments that mark hard failures we should not retry.
_HARD_FAILURE_PATTERNS = (
    "video unavailable",
    "private video",
    "members-only",
    "video is age-restricted",
    "this video is only available",
)
# yt-dlp stderr fragments that mark transient / rate-limit failures.
_RATE_LIMIT_PATTERNS = (
    "http error 429",
    "too many requests",
    "sign in to confirm you're not a bot",
    "sign in to confirm your age",
)


